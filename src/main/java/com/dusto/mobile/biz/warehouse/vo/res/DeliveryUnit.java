package com.dusto.mobile.biz.warehouse.vo.res;

import java.io.Serializable;

/**
 * Created by cwd on 6/6/2017.
 */

public class DeliveryUnit implements Serializable{
    /**
	 * 
	 */
	private static final long serialVersionUID = 4587612046154682769L;
	//销售凭证号
    private String vbeln;
    //转储单编号
    private String tanum;
    //物料号
    private String matnr;
    //数量
    private int  menge;
    //已核对数量
    private int  amount;
    
    public String getTanum() {
        return tanum;
    }

    public void setTanum(String tanum) {
        this.tanum = tanum;
    }

    public String getVbeln() {
        return vbeln;
    }

    public void setVbeln(String vbeln) {
        this.vbeln = vbeln;
    }

    public String getMatnr() {
        return matnr;
    }

    public void setMatnr(String matnr) {
        this.matnr = matnr;
    }

	public int getMenge() {
		return menge;
	}

	public void setMenge(int menge) {
		this.menge = menge;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

}
